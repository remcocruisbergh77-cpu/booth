// Service worker: cacht de app zodat 'm offline werkt op je iPhone (na de eerste keer laden).
// Werkt alleen in een beveiligde context (https of localhost).
const CACHE = 'booth-v1';
const ASSETS = ['./', './index.html', './manifest.webmanifest', './icon.png', './apple-touch-icon.png'];

self.addEventListener('install', e => {
  e.waitUntil(caches.open(CACHE).then(c => c.addAll(ASSETS)).then(() => self.skipWaiting()));
});
self.addEventListener('activate', e => {
  e.waitUntil(
    caches.keys().then(keys => Promise.all(keys.filter(k => k !== CACHE).map(k => caches.delete(k))))
      .then(() => self.clients.claim())
  );
});
self.addEventListener('fetch', e => {
  if (e.request.method !== 'GET') return;
  e.respondWith(caches.match(e.request).then(r => r || fetch(e.request)));
});
